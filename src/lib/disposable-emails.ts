/**
 * Valida si un email pertenece a un dominio de email temporal/desechable
 * @param email - Email a validar
 * @returns true si es email temporal, false si es válido
 */
export function isDisposableEmail(email: string): boolean {
  if (!email || typeof email !== 'string') {
    return false
  }

  const domain = email.toLowerCase().split('@')[1]
  if (!domain) {
    return false
  }

  return disposableEmailDomains.includes(domain)
}

/**
 * Obtiene el dominio de un email
 * @param email - Email del cual extraer el dominio
 * @returns Dominio del email o null si es inválido
 */
export function getEmailDomain(email: string): string | null {
  if (!email || typeof email !== 'string') {
    return null
  }

  const parts = email.toLowerCase().split('@')
  return parts.length === 2 ? parts[1] : null
}
